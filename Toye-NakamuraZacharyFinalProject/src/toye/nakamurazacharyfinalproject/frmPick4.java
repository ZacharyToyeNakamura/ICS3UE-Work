    /**
     * This method returns if the input string is valid or not for parsing
     * @param strValidation Any string - normally the input string
     * @return return true If the it's a int between 0 and 9999 else false
     */
    public static boolean isValid(String strValidation)
    {
        try
        {
            // if it's a int between 0 and 9999 inclusive then it's valid
            if (Integer.parseInt(strValidation) >= 0 && Integer.parseInt(strValidation) < 10000) return true;
            // otherwise it's not
            return false;
        }
        // if there is a error
        catch (Exception InputFormatException)
        {
            // its not valid
            return false;
        }
    }
    
    
    /**
     * It takes in a number and returns a string with leading zeros filled in to 4 places
     * @param intNum Any number between 0 and 9999
     * @return A string with the number input but with leading zeros so it's 4 digits
     */
    public static String leadingZeros(int intNum)
    {
        // declare the output string
        String strOutput = "" + intNum;
        // for (4- number of characters in intNum) add a 0
        for (int i = 4 - strOutput.length(); i > 0; i--) 
        {
            // add a leading 0
            strOutput = "0" + strOutput;
        }
        // return the answer
        return strOutput;
    }
    
    
