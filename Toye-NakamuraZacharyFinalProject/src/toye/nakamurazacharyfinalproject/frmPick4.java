    private void btnPlayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlayActionPerformed
        // TODO add your handling code here:
        
        // if the input it's valid
        if (!isValid(txtUserNum.getText()))
        {
            //display a error message
            lblResult.setText("Error: Please enter a number between 0000 and 9999");
            // stop
            return;
        }
        // subtract from the user's money the entry cost
        intUserMoney--;
        // get the winning number
        int intWinNum = (int)(Math.random() * 10000);
        // if the user guessed the winning number
        if (intWinNum == Integer.parseInt(txtUserNum.getText()))
        {
            // display a win message
            lblResult.setText("Against all odd you won $5000!, congratulations! the winning number was " + leadingZeros(intWinNum));
            // give the user their win money
            intUserMoney += 5000;
        }
        // if the user loses
        else
        {
            // display a lose message
            lblResult.setText("Unsuprisingly you lost, the winning number was " + leadingZeros(intWinNum));
        }  
        // display the user's new mojney total
        lblMoney.setText("Money: $" + intUserMoney);
    }//GEN-LAST:event_btnPlayActionPerformed

